#include "ft_printf.h"

int	ft_print_u(unsigned int nb)
{
	int	i;

	i = 0;
	if (nb >= 10)
		i = ft_print_u(nb / 10);
	nb = nb % 10;
	if (nb < 10)
		i += ft_putchar(nb + '0');
	return (i);
}
